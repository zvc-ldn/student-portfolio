                    <h1>The Digital Industry</h1>


                    <h2>The UK Tech Scene:</h2>

        <p>
            Despite its comparative small size on a global scale, the UK is a vital hub in the international tech ecosystem and a strong economic performer punching well above its weight, according to the Tech Nation Report of 2019. With European tech investment as a whole reaching a record high in 2018, scaleup investment in the UK followed suit increasing by an extraordinary 61% yearly since 2017. To put a monetary figure on these percentages, the value of total capital investment in UK tech peaked above £6bn for the first time to £6.3bn; the highest of all European countries (Tech Nation, 2019).
        </p>

        <img src="img/tech-nation.png" alt="desktop background: 'ZVC' logo for my design business" 
        width="100%" height="auto" />
        <p>
            (Tech Nation, 2019)
        </p>

        <p>
                Furthermore, of this £6.3bn a staggering 80% of this figure (£5bn), was invested in domestic scaleups with recent figures showing that their growth rate in London is at 56%, making it the world-leader in terms of growth (Tech Nation, 2019). As a whole scaleup tech investment was 2.5 times higher than expected based on the comparative size of the UK economy (The UK attracted 5.2% of international high-growth investment in 2018, whilst UK GDP in totality, only accounts for 2.2% of the worldwide economy) (Tech Nation, 2019). From these figures it is clear to see that the UK tech scene is undergoing rapid growth, and I hope to contribute to this in the future; which leads to my next section.
        </p>

        <h2>Young Graduates and Minorities in Tech:</h2>

        <p>
                Although the UK Tech market is swiftly growing, there is still a huge diversity gap within the current workforce, as 85% of UK tech roles are occupied by white-European ethnic groups; minority and mixed ethnicity groups account for the remaining 15% at 10% and 5% respectively (Manchester Digital Skills Audit, 2019). This makes it both exciting and nerve-wracking entering this industry as a young, mixed ethnicity male because although there are many workplace diversity schemes in place that could work in my favour, the potential lack of diversity once I set foot in the workplace could become a factor to think about. The gender gap in UK tech roles is also strong cause for concern as those who identify as women only occupy 20% of jobs in this field.
        </p>

        <img src="img/gender-roles.png" alt="desktop background: 'ZVC' logo for my design business" 
        width="100%" height="auto" />
        <p>
            (Manchester Skills Audit, 2019)
        </p>

        <p>
                The Software Guild, a global learning company based in Ohio, argue that according to their sources, diverse workplaces outclass homogenous workplaces. This was also supported by a survey conducted by the Harvard Business Review that established employees in a diverse workplace outperform and are more innovative than companies with less diverse work environments (The Software Guild, 2019). I agree with this because in order to foster innovation, creativity and new ideas, it is paramount to have different perspectives. 
        </p>
        <p>
                I believe individuals, of minority backgrounds are necessary in the tech industry as they have had different experiences to the stereotypical 30-year-old white, middle-class male website developer and can thus offer new insights into this growing industry with limitless potential, where a new idea can be the difference between a successful company and a failed one. 
        </p>
        <p>
                Not only this, but just by having a diverse workplace, it is possible for companies to shirk certain setbacks that are honestly avoidable; for example, the racism scandal which plagued UK fashion retailer H&M earlier this year. Companies in general, but also tech companies in particular are realising this now though and are starting to address their lack of diversity internally and amending their recruitment process.
        </p>

        <h2>Recruitment, Skillsets &amp; My Future Career:</h2>

        <p>
                It’s reassuring to know that in our current economy, demand for tech roles is high with 74% of businesses in the north recruiting graduates (81% of which, recruited for new positions in the last 12 months). The Manchester Digital Skills Audit 2019 also reported that 53% of respondent businesses in Greater Manchester recruit graduates from The Manchester Metropolitan University (MMU), second only to The University of Manchester with 55%. Though being in very high demand, respondents to the audit also stated developer roles were their hardest jobs to fill as not every developer is the right fit for a company (Manchester Digital Skills Audit, 2019). 
        </p>

        <img src="img/recruit-uni.png" alt="desktop background: 'ZVC' logo for my design business" 
        width="100%" height="auto" />
        <p>
            (Manchester Skills Audit, 2019)
        </p>

        <p>
                With a background in graphic design and hopes of potentially entering the tech industry as a front-end developer, I believe there are positive prospects for my future endeavours. Due to PHP and Javascript still topping the list of most in-demand programming languages, it’ll be useful to further my understanding of back-end development in the long run in order to maximise opportunities.
        </p>

